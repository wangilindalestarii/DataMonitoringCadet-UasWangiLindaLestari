    private void tableCadetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCadetMouseClicked
        // TODO add your handling code here:
        int row = tableCadet.getSelectedRow();
        labelID.setText(tableCadet.getValueAt(row, 0).toString());
        textNIT.setText(tableCadet.getValueAt(row, 1).toString());
        textNama.setText(tableCadet.getValueAt(row, 2).toString());
        textAlamat.setText(tableCadet.getValueAt(row, 3).toString());
        String prodi = tableCadet.getValueAt(row, 4).toString();
        if (prodi.equalsIgnoreCase("Teknika")){
            radioKalk.setSelected(true);
        }else{
           if(prodi.equalsIgnoreCase("Kalk")){
           radioNautik.setSelected(true);
        }else{
               radioTeknik.setSelected(true);
           }
        comboGoldar.setSelectedItem(tableCadet.getValueAt(row, 5).toString());
        buttonTambah.setEnabled(false);
        buttonUbah.setEnabled(true);
        buttonHapus.setEnabled(true);
        }
    }//GEN-LAST:event_tableCadetMouseClicked

