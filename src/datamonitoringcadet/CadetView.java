    private void tampil_data(){
        try {
            String[] judul = {"ID","NIT","NAMA LENGKAP","ALAMAT","PRODI","GOLDAR"};
            dtm = new DefaultTableModel(null, judul);
            tableCadet.setModel(dtm);
            String sql = "";
            if(textCari.getText().isEmpty()){
                sql = "select * from cadet";
            }else{
                sql = "select * from cadet where nama_lengkap like '%" + textCari.getText() +"%'";
            }
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)};
            dtm.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

