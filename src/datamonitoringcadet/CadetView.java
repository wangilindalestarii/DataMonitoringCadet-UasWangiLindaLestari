private void tambah_data() {
        try {
           String prodi = "Teknika";
           if(radioNautik.isSelected()){
               prodi = "Nautika";               
           }else{
               if(radioKalk.isSelected()){
                   prodi = "Kalk";
               }
           }
           pst = con.prepareStatement("insert into cadet values (?,?,?,?,?,?)");
           pst.setInt(1, 0);
           pst.setString(2, textNIT.getText());
           pst.setString(3, textNama.getText());
           pst.setString(4, textAlamat.getText());
           pst.setString(5, prodi);
           pst.setString(6, comboGoldar.getSelectedItem().toString());
           pst.executeUpdate();
           JOptionPane.showMessageDialog(null, "Data Cadet Berhasil ditambahkan");
           ulang();
           tampil_data();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    private void ubah_data() {
        try {
           String prodi = "Teknika";
           if(radioNautik.isSelected()){
               prodi = "Nautika";               
           }else{
               if(radioKalk.isSelected()){
                   prodi = "Kalk";
               }
           }
           pst = con.prepareStatement("update cadet set nit=?, nama_lengkap=?, alamat=?, prodi=?, goldar=?, where id=?");
           pst.setString(1, textNIT.getText());
           pst.setString(2, textNama.getText());
           pst.setString(3, textAlamat.getText());
           pst.setString(4, prodi);
           pst.setString(5, comboGoldar.getSelectedItem().toString());
           pst.setInt(6, Integer.parseInt(labelID.getText()));
           pst.executeUpdate();
           JOptionPane.showMessageDialog(null, "Data berhasil diPerbaharui");
           ulang();
           tampil_data();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
