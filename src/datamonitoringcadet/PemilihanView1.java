 private void simpan_pemilihan_detail() {
        try {
            int i=0;
            while(i<tabelCadet.getRowCount()){
                pst = con.prepareStatement("insert into pemilihan_detail (id_pemilihan, id) values (?, ?)");
                pst.setString(1, labelIDPemilihan.getText());
                pst.setString(2, tabelCadet.getValueAt(i, 0).toString());
                pst.executeUpdate();
                i++;
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    private void simpan_pemilihan_tempat() {
       try {
            SimpleDateFormat formatTanggal = new SimpleDateFormat("yyyy-MM-dd");
            pst = con.prepareStatement("insert into pemilihan_tempat values (?, ?, ?, ?)");
            pst.setString(1, labelIDPemilihan.getText());
            pst.setString(2, formatTanggal.format(tanggalMulai.getDate()));
            pst.setString(3, formatTanggal.format(tanggalSelesai.getDate()));
            pst.setString(4, labelIDTempat.getText());
            pst.executeUpdate();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
