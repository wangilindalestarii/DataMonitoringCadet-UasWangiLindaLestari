    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        if(tanggalMulai.getDate()==null){
            JOptionPane.showMessageDialog(null, "Tanggal mulai belum dipilih!", "Pilih Tanggal", 0);
            tanggalMulai.requestFocus();
        }else if(tanggalSelesai.getDate()==null){
            JOptionPane.showMessageDialog(null, "Tanggal selesai belum dipilih!", "Pilih Tanggal", 0);
            tanggalSelesai.requestFocus();
        }else if(labelIDTempat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama tempat PKL belum dipilih!", "Pilih Tempat PKL", 0);
            comboTempat.requestFocus();
        }else if(tabelCadet.getRowCount()<=0){
            JOptionPane.showMessageDialog(null, "Data mahasiswa belum ditambahkan!", "Pilih Tempat PKL", 0);
            textNIT.requestFocus();
        }else{
            try {
                simpan_pemilihan_tempat();
                simpan_pemilihan_detail();
                JOptionPane.showMessageDialog(null, "Data pemilihan tempat PKL berhasil disimpan");
                ulang();
            }catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada simpan data: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed

