    private void ButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonTambahActionPerformed
        // TODO add your handling code here:
        if(labelIDCadet.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Cadet belum ditemukan!", "Gagal Tambah", 0);
        }else{
            String[] data = {labelIDCadet.getText(), textNIT.getText(), textNama.getText(),
                textAlamat.getText(), textProdi.getText()};
            dtm.addRow(data);
            textNIT.setText("");
            textNama.setText("");
            textAlamat.setText("");
            textProdi.setText("");
            labelIDCadet.setText("");
            textNIT.requestFocus();
        }
    }//GEN-LAST:event_ButtonTambahActionPerformed

