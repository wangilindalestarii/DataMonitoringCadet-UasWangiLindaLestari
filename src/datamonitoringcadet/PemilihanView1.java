        private void get_id_pemilihan(){
        try{
            pst = con.prepareStatement("select id_pemilihan from pemilihan_tempat order by id_pemilihan desc");
            rs = pst.executeQuery();
            if(rs.next()){
                int id = rs.getInt(1)+1;
                labelIDPemilihan.setText(id+"");
            }else{
                labelIDPemilihan.setText("1");
            }
        }catch (Exception e){
            System.out.println(e.getMessage());
        }
    }
    
    private void tampil_tempat(){
        try {
            comboTempat.addItem("Pilih tempat Praktik");
            pst = con.prepareStatement("select nama_tempat from tempat_praktek");
            rs = pst.executeQuery();
            while(rs.next()){
                comboTempat.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
